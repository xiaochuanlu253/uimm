#' Fit a PAC model via a formula interface
#'
#' @param formula A survival formula. Use counting-process syntax:
#'   Surv(birthage, censoredage, DthIndicator) ~ 1
#'   (RHS must be ~ 1 for age-only models.)
#' @param data A data.frame containing the variables in the formula.
#' @param AC Character name of the age-cohort family: one of
#'   names(.pac_registry) e.g. "Gompertz", "Makeham", etc.
#' @param start Optional numeric vector of initial params to override defaults.
#' @param bounds Optional list(lower=..., upper=...) to override defaults.
#' @param patience, tol, verbose Passed through to `fit()`.
#' @return A PACFit S4 object.
pac_fit <- function(formula, data,
                    AC = c("Gompertz","Makeham","Perks","Beard","Kannisto","Thatcher"),
                    start = NULL, bounds = NULL,
                    patience = 500, tol = 1e-6, verbose = FALSE) {
  AC <- match.arg(AC)

  # Build model frame and extract Surv response
  mf <- stats::model.frame(formula, data = data, na.action = na.omit)
  y  <- stats::model.response(mf)

  # Expect counting-process Surv(start, stop, event)
  sType <- attr(y, "type")
  if (!(sType %in% c("counting"))) {
    stop("Use Surv(birthage, censoredage, DthIndicator) with counting-process syntax.")
  }
  birthage      <- y[, 1]
  censoredage   <- y[, 2]
  DthIndicator  <- y[, 3]

  # Age-only dataset expected by your current code
  df_train <- data.frame(
    birthage = as.numeric(birthage),
    censoredage = as.numeric(censoredage),
    DthIndicator = as.integer(DthIndicator)
  )

  # Instantiate the chosen PAC model
  ctor <- .pac_registry[[AC]]
  if (is.null(ctor)) stop("Unknown AC family: ", AC)
  pac_model <- ctor()

  # Optional overrides
  if (!is.null(start))  pac_model@params <- start
  if (!is.null(bounds)) pac_model@bounds <- bounds

  # Build PACFit and fit
  fit_obj <- methods::new("PACFit", model = pac_model, data = list(train = df_train, test = NULL))
  fit_obj <- fit(fit_obj, data_train = df_train, patience = patience, tol = tol, verbose = verbose)
  return(fit_obj)
}

