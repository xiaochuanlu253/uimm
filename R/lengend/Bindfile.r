mort_fit <- function(model = c("baseline","st","stp","mtp","gph"),
                     formula,
                     data = NULL,
                     data_train = NULL,
                     data_test = NULL,
                     ...) {
  if (is.data.frame(data)) {
    data_train <- data
    data_test <- NULL
    data <- list(train = data_train, test = data_test)
  }

  if (is.null(data)){
    data <- list(train = data_train, test = data_test)
  }

