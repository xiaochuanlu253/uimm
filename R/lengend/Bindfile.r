# Log-likelihood method for PAC models
setMethod("logllh", "PAC", function(model, par, x, r, i) {
  # Return -Inf if parameters are not finite or if the second parameter is too close to zero
  if (any(!is.finite(par)) || abs(par[2]) <= 1e-6) return(-Inf)
  # Compute cumulative hazard at observed time
  H <- model@cumhaz_(par, x, r - x)
  # Compute hazard at event time and at baseline
  mu_r <- model@mu_(par, r)
  mu_0 <- model@mu_(par, 0)
  # Check for invalid values
  if (any(!is.finite(H)) || any(H <= 0)) return(-Inf)
  if (any(!is.finite(mu_r)) || any(mu_r <= 0)) return(-Inf)
  if (any(!is.finite(mu_0)) || any(mu_0 <= 0)) return(-Inf)
  # Log-likelihood: i*log(mu_r) - H
  ll <- i * log(mu_r) - H
  return(ll)
})

# Method to get log-likelihood for a dataset (vectorized)
setMethod("get_logllh", "PAC", function(model, data_train, par = NULL) {
  if (is.null(par)) par <- model@params
  logllh(
    model,
    par = par,
    x = data_train$birthage,
    r = data_train$censoredage,
    i = data_train$DthIndicator
  )
})

# Fit method for PACFit: fits the model to training data using GenSA optimizer
setMethod("fit", "PACFit", function(model_fit, data_train, patience = 30, tol = 1e-6, verbose = FALSE) {
  PAC <- model_fit@model
  # Negative log-likelihood function to minimize
  raw_fn <- function(par) {
    -sum(logllh(
      PAC,
      par = par,
      x = data_train$birthage,
      r = data_train$censoredage,
      i = data_train$DthIndicator
    ))
  }
  lower <- PAC@bounds$lower
  upper <- PAC@bounds$upper
  initial <- PAC@params
  # Early stopping mechanism based on patience and tolerance
  patience_tracker <- local({
    best_val <- Inf; best_par <- initial; counter <- 0; done <- FALSE
    function(par) {
      if (done) return(1e10)
      val <- raw_fn(par)
      if ((best_val - val) > tol) {
        best_val <<- val; best_par <<- par; counter <<- 0
      } else {
        counter <<- counter + 1
      }
      if (counter >= patience) done <<- TRUE
      return(val)
    }
  })
  # Run the GenSA optimizer
  result <- GenSA(
    par = initial,
    fn = patience_tracker,
    lower = lower,
    upper = upper,
    control = list(max.call = 2000, verbose = verbose)
  )
  # Update model parameters and fit status
  model_fit@model@params <- result$par
  model_fit@fitted <- TRUE
  model_fit@fit_results <- result
  return(model_fit)
})

# AIC method for PACFit: computes Akaike Information Criterion for fitted model
setMethod("AIC", "PACFit", function(model_fit) {
  if (!model_fit@fitted) stop("Model has not been fitted yet.")
  k <- length(model_fit@model@params)
  loglik <- -model_fit@fit_results$value
  aic <- -2 * loglik + 2 * k
  return(aic)
})

