# --- Gompertz ---
# Survival function for the Gompertz model
survival_gompertz <- function(par, x) {
  a <- par[1]; b <- par[2]
  exp(-exp(a) * (exp(b * x) - 1) / b)
}
# Cumulative hazard function for the Gompertz model
cumulative_hazard_gompertz <- function(par, x, t) {
  a <- par[1]; b <- par[2]
  (exp(b * t) - 1) / b * exp(a + b * x)
}
# Force of mortality (hazard) for the Gompertz model
forceofmortality_gompertz <- function(par, x) {
  a <- par[1]; b <- par[2]
  exp(a + b * x)
}

# --- Gompertz-Makeham ---
# Survival function for the Gompertz-Makeham model
survival_makeham <- function(par, x) {
  a <- par[1]; b <- par[2]; lambda <- par[3]
  exp(-exp(a) * (exp(b * x) - 1) / b - x * lambda)
}
# Cumulative hazard function for the Gompertz-Makeham model
cumulative_hazard_makeham <- function(par, x, t) {
  a <- par[1]; b <- par[2]; lambda <- par[3]
  (exp(b * t) - 1) / b * exp(a + b * x) + lambda * t
}
# Force of mortality (hazard) for the Gompertz-Makeham model
forceofmortality_makeham <- function(par, x) {
  a <- par[1]; b <- par[2]; lambda <- par[3]
  exp(a + b * x) + lambda
}

# --- Perks ---
# Survival function for the Perks model
survival_perks <- function(par, x) {
  a <- par[1]; b <- par[2]; gam <- par[3]; lambda <- par[4]
  ((1 + exp(a + gam)) / (1 + exp(a + gam + b * x)))^(exp(-gam) / b) / exp(lambda * x)
}
# Cumulative hazard function for the Perks model
cumulative_hazard_perks <- function(par, x, t) {
  a <- par[1]; b <- par[2]; gam <- par[3]; lambda <- par[4]
  # Different forms depending on b for numerical stability
  if (b > 2) {
    tolog = exp(b * t) + exp(-(a + gam + b * (x - t)))
  } else if (b > 1) {
    tolog = exp(b * t) + (exp(-b * t) - 1) / (exp(-b * t) + exp(a + gam + b * (x - t)))
  } else {
    tolog = (1 + exp(a + gam + b * (x + t))) / (1 + exp(a + gam + b * x))
  }
  exp(-gam) / b * log(tolog) + lambda * t
}
# Force of mortality (hazard) for the Perks model
forceofmortality_perks <- function(par, x) {
  a <- par[1]; b <- par[2]; gam <- par[3]; lambda <- par[4]
  if (b > 1) {
    1 / (exp(-(a + b * x)) + exp(gam)) + lambda
  } else {
    exp(a + b * x) / (1 + exp(a + gam + b * x)) + lambda
  }
}

