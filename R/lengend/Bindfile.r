# ---- Integrated Brier Score (IBS) ----
# Computes empirical survival curves for a dataset and caches them in Surv_info_total.
# Used for Brier Score calculation.
empirical_survival <- function(data, data_name, x_min = 80, x_max = 115) {
  if (!exists("Surv_info_total")) stop("Surv_info_total is not defined in the global environment.")
  if (exists(data_name, where=Surv_info_total)) return(Surv_info_total[[data_name]])
  # Survival for birth age (all events)
  Surv_BA = Surv(time = data$birthage, event = rep(1, nrow(data)))
  survfit_BA = survfit(Surv_BA ~ 1)
  sf_BA = summary(survfit_BA, times = x_min:x_max)$surv
  # Survival for censored age (non-deaths)
  Surv_CA = Surv(time = data$censoredage, event = 1 - data$DthIndicator)
  survfit_CA = survfit(Surv_CA ~ 1)
  sf_CA = summary(survfit_CA, times = x_min:x_max)$surv
  # Store in global cache
  Surv_info_total[[data_name]] <<- list(
    survfit_BA = survfit_BA,
    survfit_CA = survfit_CA,
    sf_BA = sf_BA,
    sf_CA = sf_CA
  )
  save(Surv_info_total, file = paste0("Surv_info_total.RData"))
  return(Surv_info_total[[data_name]])
}

# ---- Brier Score (BS) for a single time/individual ----
# Calculates the Brier Score for a given age, individual, and model.
BS_raw <- function(x, birthage, censoredage, DthIndicator, Sfunc, par, Surv_info, x_min = 80) {
  S <- Sfunc(par, x)
  S0 <- Sfunc(par, birthage)
  S <- S / S0
  if (x < birthage) {
    bs <- 0
  } else if (censoredage <= x && DthIndicator == 1) {
    # Event occurred before or at time x
    rawbs <- S^2
    CA_survfit <- Surv_info$survfit_CA
    if (max(Surv_info$survfit_CA$time) <= censoredage){
      SCA = 1 / CA_survfit$n
    }
    else{
      SCA <- summary(CA_survfit, times = censoredage)$surv
    }
    bs <- rawbs / SCA
  } else if (censoredage > x) {
    # Censored after time x
    rawbs <- (1 - S)^2
    idx <- x - x_min + 1
    sfBA <- Surv_info$sf_BA[idx]
    sfCA <- Surv_info$sf_CA[idx]
    bs <- rawbs / (1 - sfBA) / sfCA
    # Fallback for numerical issues
    if (!is.finite(bs) || is.nan(bs) || is.na(bs)) {
      if (max(Surv_info$survfit_BA$time) <= x) {
        sfBA = 0
      }
      if (max(Surv_info$survfit_CA$time) <= x) {
        sfCA = 1 / Surv_info$survfit_CA$n
      }
      bs <- rawbs / (1 - sfBA) / sfCA
    }
  } else {
    bs <- 0
  }
  return(bs)
}

# ---- Integrated Brier Score (IBS) for a dataset ----
# Applies BS_raw across ages 80:115 and all individuals, then averages.
setMethod("BS", "PACFit", function(model_fit, data_valid, Surv_info, x_min = 80, x_max = 115) {
  if (!model_fit@fitted) stop("Model has not been fitted yet.")
  bs_values <- c()
  for (age in c(x_min:x_max)) {
    tempbs <-
      mapply(
        function(birthage, censoredage, DthIndicator) {
          BS_raw(
            x = age,
            birthage = birthage,
            censoredage = censoredage,
            DthIndicator = DthIndicator,
            Sfunc = model_fit@model@survival_,
            par = model_fit@model@params,
            Surv_info = Surv_info
          )
        },
        birthage = data_valid$birthage,
        censoredage = data_valid$censoredage,
        DthIndicator = data_valid$DthIndicator
      )
    bs_values <- bs_values %>% c(mean(tempbs))
  }
  return(mean(bs_values))
}
