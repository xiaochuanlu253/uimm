#' @describeIn PACFit
#' Custom initializer for PACFit objects.
#'
#' Assigns the model and data slots, handling both combined data lists and separate train/test data frames.
#' Initializes fit_results as an empty list and sets fitted to FALSE.
#'
#' @param .Object The PACFit object being initialized.
#' @param model A PAC model object.
#' @param data Optional list containing 'train' and 'test' data frames.
#' @param data_train Optional training data frame.
#' @param data_test Optional test data frame.
#' @return The initialized PACFit object.
setMethod("initialize", "PACFit", function(.Object, model, data=NULL, data_train=NULL, data_test=NULL) {
  .Object@model <- model
  if (!is.null(data)) {
    .Object@data <- data
    .Object@data_train <- if (is.null(data$train)) NULL else data$train
    .Object@data_test  <- if (is.null(data$test))  NULL else data$test
  } else {
    .Object@data <- if (!is.null(data_train) || !is.null(data_test)) list(train=data_train, test=data_test) else NULL
    .Object@data_train <- data_train
    .Object@data_test  <- data_test
  }
  .Object@fit_results <- list()
  .Object@fitted <- FALSE
  .Object
})

