#' Predict helper (optional but handy)
#' @param object PACFit
#' @param newdata data.frame with birthage and age columns
#' @param type "survival"|"hazard"|"loglik"
#' @param age_col column name for absolute age at which to evaluate
#' @return numeric vector
predict.PACFit <- function(object, newdata,
                           type = c("survival","hazard","loglik"),
                           age_range = 80:115) {
  # Basic checks
  if (!isTRUE(object@fitted)) stop("PACFit object is not fitted.")
  if (!is.data.frame(newdata)) stop("'newdata' must be a data.frame.")
  type <- match.arg(type)

  ages <- sort(as.numeric(age_range))
  if (any(!is.finite(ages))) stop("'age_range' must be finite numerics.")

  par <- object@model@params
  n   <- nrow(newdata)
  rn  <- rownames(newdata); if (is.null(rn)) rn <- as.character(seq_len(n))

  if (type == "survival") {
    if (!("birthage" %in% names(newdata)))
      stop("When type='survival', newdata must contain a 'birthage' column.")

    ba <- as.numeric(newdata$birthage)
    if (any(!is.finite(ba))) stop("'birthage' must be finite numerics.")

    # Unconditional survival at grid ages and at birth ages
    S_age <- object@model@survival_(par, ages)   # length = |ages|
    S_ba  <- object@model@survival_(par, ba)     # length = n

    # Conditional survival matrix S(a|b) = S(a)/S(b)
    M <- matrix(rep(S_age, each = n), nrow = n)
    M <- sweep(M, 1, S_ba, "/")

    # Ages younger than birth age => S = 1
    for (i in seq_len(n)) {
      younger_cols <- which(ages < ba[i])
      if (length(younger_cols)) M[i, younger_cols] <- 1
    }

    M[!is.finite(M)] <- NA_real_
    M <- pmin(pmax(M, 0), 1)
    colnames(M) <- as.character(ages)
    rownames(M) <- rn
    return(M)
  }

  if (type == "hazard") {
    mu_age <- object@model@mu_(par, ages)        # length = |ages|
    M <- matrix(rep(mu_age, each = n), nrow = n)
    M[!is.finite(M)] <- NA_real_
    colnames(M) <- as.character(ages)
    rownames(M) <- rn
    return(M)
  }

  # type == "loglik" : per-individual log-likelihood contribution
  req <- c("birthage","censoredage","DthIndicator")
  missing_cols <- setdiff(req, names(newdata))
  if (length(missing_cols))
    stop("When type='loglik', newdata must contain columns: ",
         paste(req, collapse = ", "), ". Missing: ",
         paste(missing_cols, collapse = ", "))

  ba  <- as.numeric(newdata$birthage)
  ca  <- as.numeric(newdata$censoredage)
  di  <- as.integer(newdata$DthIndicator)

  if (any(!is.finite(ba)) || any(!is.finite(ca)) || any(!is.finite(di)))
    stop("birthage, censoredage, and DthIndicator must be finite (and integer for DthIndicator).")

  ll_vec <- logllh(object@model, par = par, x = ba, r = ca, i = di)
  names(ll_vec) <- rn
  return(ll_vec)
}

#' Evaluate a fitted PAC model on a dataset (incl. predict-based IBS)
#'
#' @param object Fitted PACFit object.
#' @param newdata data.frame with columns birthage, censoredage, DthIndicator.
#' @param metrics Character vector among:
#'   c("AIC","loglik_sum","loglik_vec","IBS").
#' @param ibs_method "predict" (default; masking-only per your rules) or "ipcw" (uses BS()).
#' @param age_range Integer/numeric vector of absolute ages for IBS grid (predict-based).
#' @param normalize_IBS If TRUE, return mean squared error (else sum).
#' @param surv_info Optional (only for ibs_method="ipcw").
#' @param surv_from "newdata" or "train" (only for ibs_method="ipcw").
#' @param data_name Optional cache key for empirical_survival (only for ibs_method="ipcw").
#' @return A named list of requested metrics. For IBS (predict), also returns IBS_sum and IBS_by_age.
evaluate.PACFit <- function(object, newdata,
                            metrics = c("AIC","loglik_sum","loglik_vec","IBS"),
                            ibs_method = c("predict","ipcw"),
                            age_range = 80:115,
                            surv_info = NULL,
                            surv_from = c("newdata","train"),
                            data_name = NULL) {
  if (!isTRUE(object@fitted)) stop("PACFit object is not fitted.")
  if (!is.data.frame(newdata)) stop("'newdata' must be a data.frame.")
  req_cols <- c("birthage","censoredage","DthIndicator")
  miss <- setdiff(req_cols, names(newdata))
  if (length(miss)) stop("Missing required columns in newdata: ", paste(miss, collapse = ", "))

  ibs_method <- match.arg(ibs_method)
  surv_from  <- match.arg(surv_from)
  out <- list()

  if ("AIC" %in% metrics) {
    out$AIC <- tryCatch(AIC(object), error = function(e) NA_real_)
  }

  if ("loglik_vec" %in% metrics || "loglik_sum" %in% metrics) {
    ll_vec <- tryCatch(
      get_logllh(object@model, data_train = newdata, par = object@model@params),
      error = function(e) rep(NA_real_, nrow(newdata))
    )
    if ("loglik_vec" %in% metrics) out$loglik_vec <- ll_vec
    if ("loglik_sum" %in% metrics) out$loglik_sum <- sum(ll_vec)
  }

  if ("IBS" %in% metrics) {
    if (ibs_method == "predict") {
      # ===== Predict-based IBS (masking rules) =====
      ages <- sort(as.numeric(age_range))
      n <- nrow(newdata); m <- length(ages)

      # Predicted conditional survival S_pred(i, x) = S(x)/S(birthage_i)
      S_pred <- predict.PACFit(object, newdata, type = "survival", age_range = ages)  # n x m

      # Broadcast inputs
      CA  <- matrix(as.numeric(newdata$censoredage),  n, m)
      DI  <- matrix(as.integer(newdata$DthIndicator), n, m)
      AGE <- matrix(rep(ages, each = n), n, m)

      # Empirical survival (your rules):
      # S_emp = 0 if x <= censoredage & D==1; 1 if x < censoredage; NA otherwise
      S_emp <- ifelse(AGE <= CA & DI == 1L, 0,
                      ifelse(AGE < CA, 1, 0))

      # Weights: w = 0 if x > censoredage & D==0; 1 otherwise
      w <- ifelse(AGE > CA & DI == 0L, 0, 1)

      # Squared error with masking
      diff2 <- (S_pred - S_emp)^2
      diff2[!is.finite(diff2)] <- NA_real_
      contrib <- diff2 * w

      # Overall IBS (mean over weighted elements if normalize_IBS=TRUE)
      denom_total <- sum(w, na.rm = TRUE)
      IBS_sum     <- sum(contrib, na.rm = TRUE)
      IBS_mean    <- IBS_sum / max(1, denom_total)

      # ----- Age-weighted IBS_by_age -----
      # Exposure (how many weighted elements contribute) at each age
      denom_age   <- colSums(w, na.rm = TRUE) # length m
      by_age_mean <- colSums(contrib, na.rm = TRUE) / pmax(1, denom_age)

      # Weight the per-age means by exposure share so they sum to IBS (when normalized)
      weights_age <- denom_age / max(1, denom_total) # sum(weights_age) == 1
      IBS_part_age  <- by_age_mean * weights_age # sum == IBS_mean
      # Unnormalized: contributions per age (sum equals IBS_sum)
      IBS_by_age  <- by_age_mean # equals colSums(contrib)

      names(IBS_by_age) <- as.character(ages)

      #out$S_emp <- S_emp
      #out$S_pred <- S_pred
      #out$w <- w

      out$IBS <- IBS_mean
      out$IBS_part_age <- IBS_part_age
      out$IBS_by_age <- IBS_by_age

    } else {
      # ===== IPCW/KM-based IBS (legacy BS()) =====
      if (is.null(surv_info)) {
        src <- switch(
          surv_from,
          newdata = newdata,
          train   = if (!is.null(object@data) && !is.null(object@data$train)) object@data$train else newdata
        )
        if (is.null(data_name)) {
          data_name <- paste0("eval_", if (!is.null(newdata$data)) unique(newdata$data)[1] else "newdata")
        }
        surv_info <- tryCatch(empirical_survival(src, data_name = data_name), error = function(e) NULL)
      }
      out$IBS <- tryCatch({
        if (is.null(surv_info)) stop("surv_info unavailable")
        BS(object, data_valid = newdata, Surv_info = surv_info)
      }, error = function(e) NA_real_)
      # No IBS_by_age from IPCW path
    }
  }

  out
}

